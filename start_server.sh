#!/bin/bash

# 获取本机IP地址 (macOS)
IP=$(ipconfig getifaddr en0)

echo "=================================================="
echo "🏥 医院查询系统 - 局域网启动脚本"
echo "=================================================="
echo "正在启动服务..."
echo ""
echo "请在同一局域网下的手机或电脑浏览器中访问："
echo "👉 http://$IP:8000"
echo ""
echo "按 Ctrl+C 停止服务"
echo "=================================================="

# 启动 Python 服务器
python3 -m http.server 8000 --bind 0.0.0.0